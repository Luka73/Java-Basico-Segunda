package Aula01;

public class Operacoes {
    public static void main(String[] args) {
        System.out.println(5 + 4); //soma --> 9
        System.out.println(5 - 4); //substrair --> 1
        System.out.println(5 * 4); //multiplição --> 20
        System.out.println(5 / 4); //divisão --> 1.25
        System.out.println(5.0 / 4); //divisão --> 1.25
        // / --> divisão (quociente)
        System.out.println(6 % 4); //módulo --> 2

        Math.sqrt(49); // raiz quadrada --> 7
        Math.pow(4, 2); //Potência --> 16
        Math.max(7, 6); // Maior --> 7
    }
}
