package Aula04;

public class EstruturasDeRepeticao2 {
    public static void main(String[] args) {
        int i = 1; // --> início
        while(i <= 10) { // --> condição
            System.out.println("Olá, Mundo!");
            i++; // --> incremento
        }

        System.out.println("*************************");

        for(int j = 1; j <= 10; j++) {
            System.out.println("Olá, Mundo!");
        }

    }
}
